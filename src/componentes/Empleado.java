package componentes;

public class Empleado {

    private int idEmpleado;
    private String nombreEmpleado, catEmpleado, nombreEmpresa, dniEmpleado, turnoEmpleado;
    private int sueldoEmpleado;

    public Empleado (int idEmpleado, String nombreEmpleado, String catEmpleado, String nombreEmpresa, String dniEmpleado, String turnoEmpleado, int sueldoEmpleado){

        this.idEmpleado = idEmpleado;
        this.nombreEmpleado = nombreEmpleado;
        this.catEmpleado = catEmpleado;
        this.nombreEmpresa = nombreEmpresa;
        this.dniEmpleado = dniEmpleado;
        this.turnoEmpleado = turnoEmpleado;
        this.sueldoEmpleado = sueldoEmpleado;
    }
    

}
